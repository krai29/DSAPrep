package com.krai29.string;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.123f;
        System.out.printf("Formatted number is %.2f%n",a);
        System.out.printf("Value of PI is %.3f%n",Math.PI);
        System.out.printf("I am %s and I am %s","Karan","Cool");
    }
}
